public class metodo {
    public static String AoB(double A, double B){
        if (A<B){
            String tiki="A es menor a B";
            return tiki;
        } else if (A>B){
            String tiki="A es mayor a B";
            return tiki;
        } else{
            String tiki="A es igual a B";
            return tiki;
        }
    }
    
}
