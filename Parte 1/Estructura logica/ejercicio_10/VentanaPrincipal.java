    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // Para calcular todo:
        String nombre;
        double inscripcion, patri,estrato,matricula;
        inscripcion=Double.parseDouble(txtInscripcion.getText());
        patri=Double.parseDouble(txtPatri.getText());
        estrato=Double.parseDouble(txtEstrato.getText());
        nombre=txtNombre.getText();
        matricula=metodo.Matricula(patri, estrato);
        txtResultado.setText("El estudiante con Inscripcion "+inscripcion+" y nombre "+nombre+" debe pagar $"+matricula);
        
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

